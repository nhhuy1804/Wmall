<M002GetNoticeBSM130T>
SELECT		SUBSTR(DD_START,1,4) || '-' || SUBSTR(DD_START,5,2) || '-' || SUBSTR(DD_START,7,2) AS DD_START
,			NO_SEQ
,			NM_TITLE
,			NM_SEND_USER
FROM		BSM130T
WHERE		IFNULL(DD_START,STRFTIME('%Y%m%d', 'NOW'))		<= @DD_SALE
AND			IFNULL(DD_END,STRFTIME('%Y%m%d', 'NOW'))		>= @DD_SALE
ORDER BY	FG_URGT		DESC
,			DD_START	DESC
,			NO_SEQ		DESC
</M002GetNoticeBSM130T>
<M002GetNoticeYNBSM130T>
SELECT SUM(CASE WHEN B.NO_SEQ IS NULL THEN 1 ELSE 0 END)
FROM BSM130T A
	LEFT JOIN BSM131T B ON A.DD_START = B.DD_START AND A.NO_SEQ = B.NO_SEQ AND B.ID_USER = @ID_USER
WHERE IFNULL(A.DD_START,STRFTIME('%Y%m%d', 'NOW'))	<= @DD_SALE
	  AND IFNULL(A.DD_END,STRFTIME('%Y%m%d', 'NOW'))		>= @DD_SALE
</M002GetNoticeYNBSM130T>
<POSClearSensData90DaysSelect>
SELECT DD_SALE, CD_STORE, NO_POS, NO_TRXN, SQ_TRXN, VC_CONT
FROM SAT010T
WHERE CD_STORE = @CD_STORE AND NO_POS = @NO_POS AND SUBSTR(VC_CONT, 1, 3) IN (@BSK_TYPE1, @BSK_TYPE2) AND DD_SALE < @TO_DATE AND DD_SALE >= @FR_DATE;
</POSClearSensData90DaysSelect>
<POSClearSensData90DaysUpdate>
UPDATE SAT010T
SET VC_CONT = @VC_CONT
WHERE CD_STORE = @CD_STORE AND NO_POS = @NO_POS AND DD_SALE = @DD_SALE AND NO_TRXN = @NO_TRXN AND SQ_TRXN = @SQ_TRXN
</POSClearSensData90DaysUpdate>
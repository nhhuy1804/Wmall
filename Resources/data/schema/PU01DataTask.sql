<GetNextSAT010TUpload>
SELECT A.DD_SALE, A.CD_STORE, A.NO_POS, A.NO_TRXN, A.SQ_TRXN, A.VC_CONT
FROM SAT010T A
	INNER JOIN 
	(SELECT DD_SALE, CD_STORE, NO_POS, NO_TRXN
		FROM SAT011T 
		WHERE FG_PRS <> 'Y'
				AND CD_STORE = @CD_STORE AND NO_POS = @NO_POS
		ORDER BY CD_STORE, NO_POS, DD_SALE, NO_TRXN
		LIMIT 1
	) B ON A.DD_SALE = B.DD_SALE AND
			A.CD_STORE = B.CD_STORE AND
			A.NO_POS = B.NO_POS AND
			A.NO_TRXN = B.NO_TRXN
ORDER BY A.CD_STORE, A.NO_POS, A.DD_SALE, A.NO_TRXN, A.SQ_TRXN
</GetNextSAT010TUpload>

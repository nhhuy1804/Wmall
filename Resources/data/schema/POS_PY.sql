<P007GetTicketGFM050T>
SELECT		KD_GIFT
,			NM_GIFT
FROM		GFM050T
WHERE		IFNULL(DD_START,STRFTIME('%Y%m%d', 'NOW'))	<= @DD_SALE
AND			IFNULL(DD_END,STRFTIME('%Y%m%d', 'NOW'))	>= @DD_SALE
;

SELECT		A.KD_GIFT
,			A.NM_GIFT
,			A.CD_PREF
,			A.CD_PO_NOTE
,			A.CD_LN_NOTE
,			A.CD_LN_GIFT
,			IFNULL(A.FG_JOIN,'0') AS FG_JOIN
,			IFNULL(B.CD_NOTE, '') AS CD_NOTE
,			CAST(IFNULL(B.AM_NOTE, 0) AS TEXT) AS AM_NOTE
FROM		GFM050T	A
LEFT JOIN	GFM051T	B	ON A.KD_GIFT = B.KD_GIFT
WHERE		IFNULL(A.DD_START,STRFTIME('%Y%m%d', 'NOW'))	<= @DD_SALE
AND			IFNULL(A.DD_END,STRFTIME('%Y%m%d', 'NOW'))		>= @DD_SALE
;
</P007GetTicketGFM050T>

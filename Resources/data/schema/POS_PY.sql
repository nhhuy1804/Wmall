<P012GetCouponBSM200T>
SELECT		NO_COUPON
,			NM_COUPON
,			FG_LIMIT
,			DD_START_DELAY
,			DD_END_DELAY
,			AM_MAX
,			FG_DC
,			CASE WHEN FG_DC = '1' THEN CAST(AM_DC AS TEXT) || '원' ELSE CAST(AM_DC AS TEXT) || '%' END AS AM_DC
FROM		BSM200T 
WHERE		IFNULL(DD_START_DELAY,STRFTIME('%Y%m%d', 'NOW'))	<= @DD_SALE
AND			IFNULL(DD_END_DELAY,STRFTIME('%Y%m%d', 'NOW'))		>= @DD_SALE
</P012GetCouponBSM200T>


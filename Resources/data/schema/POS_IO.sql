<M001SelectSAT300T>
SELECT		CAST(AM_ITEM AS TEXT) AS AM_ITEM
,			IFNULL(CT_ITEM,0) + 1 AS CT_ITEM
FROM		SAT300T
WHERE		DD_SALE		= @DD_SALE 
AND			CD_STORE	= @CD_STORE
AND			NO_POS		= @NO_POS 
AND			ID_ITEM		= @ID_ITEM
ORDER BY	CT_ITEM
LIMIT 1		
</M001SelectSAT300T>
<SelectGFM050T>
SELECT		KD_GIFT
,			NM_GIFT
FROM		GFM050T
WHERE		IFNULL(DD_START,STRFTIME('%Y%m%d', 'NOW'))	<= @DD_SALE
AND			IFNULL(DD_END,STRFTIME('%Y%m%d', 'NOW'))	>= @DD_SALE
ORDER BY	KD_GIFT
</SelectGFM050T>
<M002SelectTotalAmtSAT300T>
SELECT 		ID_ITEM
,			CAST(AM_ITEM AS TEXT) AS AM_ITEM
,			CAST(CT_ITEM AS TEXT) AS CT_ITEM
FROM 		SAT300T
WHERE 		DD_SALE 	= 	@DD_SALE 
AND			CD_STORE 	= 	@CD_STORE 
AND 		NO_POS 		= 	@NO_POS 
AND			ID_ITEM 	IN	('E00','E01','E02','E03','E04','E05','E21','E22','E23','E24','E25')
ORDER BY	ID_ITEM, CT_ITEM
</M002SelectTotalAmtSAT300T>